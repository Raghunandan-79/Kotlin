package org.example.section5OperatorsAndBooleans

fun main() {
    val a = 5
    val b = 2

    println("$a > $b = ${a > b}")
    println("$a < $b = ${a < b}")
    println("$a >= $b = ${a >= b}")
    println("$a <= $b = ${a <= b}")
    println("$a == $b = ${a == b}")
    println("$a != $b = ${a != b}")
}