package org.example.section5OperatorsAndBooleans

fun main() {
    // Addition
    println(5 + 2)
    println("Hi" + " " + "John")

    // Subtraction
    println(5 - 2)

    // Multiplication
    println(5 * 2)

    // Division
    println(5 / 2)
    println(5f / 2f)

    // Modulus (Remainder)
    println(5 % 2)

    var cats = 5
    println(cats++) // 5
    println(cats) // 6
    println(++cats) // 7

    println(cats) // 7

    println(cats--) // 7
    println(cats) // 6
    println(--cats) // 5
    println(cats) // 5
}